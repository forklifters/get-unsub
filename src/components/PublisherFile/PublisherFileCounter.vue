<template>
    <div>
        <!-- file not uploaded -->
        <v-row class="option-row d-flex" v-if="!isUploaded">
            <v-col cols="1" class="option-icon text-right">
                <v-icon class="mt-1" color="error">mdi-alert-circle</v-icon>
            </v-col>
            <v-col cols="9">
                <div class="title">
                    No COUNTER data.
                </div>
                <div class="body-2">
                    Forecasting is unavailable until you upload your COUNTER report.
                </div>
                <publisher-file-upload file-type="counter"/>
            </v-col>
            <v-col cols="2" class="text-right">
                <div class="title">0</div>
                <div class="body-2">Journals</div>
            </v-col>
        </v-row>

        <!-- file uploaded -->
        <v-row class="option-row mb-6" v-if="isUploaded">
            <v-col cols="1" class="option-icon text-right">
                <v-icon color="success" class="mt-1">mdi-checkbox-marked</v-icon>
            </v-col>
            <v-col cols="9" class="option-content">
                <div class="option-top-content d-flex">
                    <div class="option-top-content-left">
                        <div class="title">
                            COUNTER report
                        </div>
                        <div class="body-2">
                            Forecasting is customized based on your institution's downloads.
                        </div>
                    </div>
                </div>
                <v-divider class="my-2" />
                <div class="option-file-info">
                    <div class="body-2">
                        {{ myFileInfo.rows_count }} journals uploaded. Of these, {{ numRowsIgnored }} were ignored:
                        <span>
                            {{ myFileInfo.error_rows.length }} with input errors,
                            {{ ignoredOa.length }} fully Open-Access,
                            {{ ignoredMoved.length }}  no longer published by {{ publisherName }},
                            {{ ignoredInactive.length }} no longer published at all.
                        </span>
                    </div>
                    <div class="mt-4">
                        <publisher-file-delete file-type="counter"/>
                    </div>

                </div>


            </v-col>
            <v-col cols="2">
                <div class="text-right">
                    <div class="title">
                        {{ myJournals.length }}
                    </div>
                    <div class="body-2">
                        Journals
                    </div>
                </div>

            </v-col>

        </v-row>

    </div>
</template>


<script>
    import _ from "lodash"
    import {mapGetters, mapMutations, mapActions} from 'vuex'
    import {api} from "../../api";
    import PublisherFileUpload from "../PublisherFile/PublisherFileUpload";
    import PublisherFileJournalsList from "./PublisherFileJournalsList";
    import PublisherFileOption from "./PublisherFileOption";
    import PublisherFileDelete from "./PublisherFileDelete";


    export default {
        name: "PublisherFileCounter",
        components: {
            PublisherFileUpload,
            PublisherFileJournalsList,
            PublisherFileOption,
            PublisherFileDelete,
        },
        props: {},
        data() {
            return {
                dialogs: {
                    uploadFile: false,
                    deleteFile: false,
                },
                snackbars: {
                    uploadSuccess: false,
                    deleteSuccess: false
                },
            }
        },
        computed: {
            ...mapGetters([
                "publisherName",
                "publisherId",
                "publisherBigDealCost",
                "publisherJournals",
                "publisherJournalsValid",
                "publisherFiles",
            ]),
            myFileInfo() {
                return this.publisherFiles.find(f => f.id === "counter")
            },
            isUploaded() {
                return this.myFileInfo.uploaded
            },
            myJournals() {
                return this.publisherJournalsValid
            },
            numRowsIgnored() {
                return this.myFileInfo.rows_count - this.myJournals.length
            },
            ignoredOa() {
                return this.publisherJournals.filter(j => j.isOa)
            },
            ignoredMoved() {
                return this.publisherJournals.filter(j => j.isMoved)
            },
            ignoredInactive() {
                return this.publisherJournals.filter(j => j.isInactive)
            },


        },
        methods: {
            clickOption(option) {
                console.log("clickOption", option)
                if (option.isSelected) return
                if (option.isDefault) this.dialogs.deleteFile = true
                else this.dialogs.uploadFile = true
            },
        },
        created() {
        },
        mounted() {
        },
        watch: {}
    }
</script>

